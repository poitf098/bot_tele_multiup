# Function to calculate all IPs in a /24 range
function get_all_ips {
    local baseIP=$1
    local prefix=$2

    if [ "$prefix" -ne 24 ]; then
        echo "This script currently only supports /24 ranges."
        exit 1
    fi

    local ipParts=(${baseIP//./ })
    local base="${ipParts[0]}.${ipParts[1]}.${ipParts[2]}."
    local ips=()

    for i in $(seq 0 255); do
        ips+=("$base$i")
    done

    echo "${ips[@]}"
}

# Function to ping an IP with a 100 ms timeout
function ping_ip {
    local ip=$1

    if ping -c 1 -W 1 "$ip" &>/dev/null; then
        return 0
    else
        return 1
    fi
}

